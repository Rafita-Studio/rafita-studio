function slugify(value) {
  return String(value || "proyecto")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "") || "proyecto";
}

function stripMarkdown(value) {
  return String(value || "")
    .replace(/```[\s\S]*?```/g, " ")
    .replace(/`([^`]+)`/g, "$1")
    .replace(/!\[[^\]]*]\([^)]+\)/g, " ")
    .replace(/\[([^\]]+)]\([^)]+\)/g, "$1")
    .replace(/[#>*_~\-]+/g, " ")
    .replace(/\s+/g, " ")
    .replace(/\s+([,.!?;:])/g, "$1")
    .trim();
}

function summarize(value, maxLength = 160) {
  const text = stripMarkdown(value);
  if (text.length <= maxLength) return text;
  return `${text.slice(0, maxLength).trim().replace(/[,.!?;:]?$/, "")}...`;
}

function escapeHtml(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function normalizeGallery(gallery, title) {
  if (!Array.isArray(gallery)) return [];

  return gallery
    .map((item, index) => {
      if (typeof item === "string") {
        return {
          image: item,
          alt: `${title || "Proyecto"} ${index + 1}`,
        };
      }

      if (item && typeof item === "object") {
        const image = item.image || item.src || item.url || "";
        if (!image) return null;
        return {
          image,
          alt: item.alt || item.title || `${title || "Proyecto"} ${index + 1}`,
        };
      }

      return null;
    })
    .filter(Boolean);
}

function normalizeBoolean(value, defaultValue = false) {
  if (typeof value === "boolean") return value;
  if (typeof value === "string") {
    return value.trim().toLowerCase() === "true";
  }
  return defaultValue;
}

function normalizeProject(filePath) {
  const raw = fs.readFileSync(filePath, "utf8");
  const parsed = matter(raw);
  const data = parsed.data || {};
  const title = data.title || path.basename(filePath, path.extname(filePath));
  const slug = slugify(data.slug || title);
  const gallery = normalizeGallery(data.gallery, title);
  const cover = data.cover || gallery[0]?.image || "";
  const year = data.year === undefined || data.year === null || data.year === "" ? "" : Number(data.year) || data.year;
  const body = parsed.content || "";

  return {
    title,
    slug,
    url: `/pages/proyectos/${slug}.html`,
    client: data.client || "",
    year,
    category: data.category || "",
    description: data.description || summarize(body),
    cover,
    gallery,
    order: data.order === undefined || data.order === null || data.order === "" ? null : Number(data.order),
    body,
    bodyHtml: marked.parse(body),
    published: normalizeBoolean(data.published, true),
    featured: normalizeBoolean(data.featured, false),
  };
}

function sortProjects(a, b) {
  const aHasOrder = Number.isFinite(a.order);
  const bHasOrder = Number.isFinite(b.order);
  if (aHasOrder && bHasOrder && a.order !== b.order) return a.order - b.order;
  if (aHasOrder !== bHasOrder) return aHasOrder ? -1 : 1;

  const aYear = Number(a.year);
  const bYear = Number(b.year);
  const aHasYear = Number.isFinite(aYear);
  const bHasYear = Number.isFinite(bYear);
  if (aHasYear && bHasYear && aYear !== bYear) return bYear - aYear;
  if (aHasYear !== bHasYear) return aHasYear ? -1 : 1;

  return String(a.title).localeCompare(String(b.title), "es", { sensitivity: "base" });
}

function pathForProjectPage(value) {
  if (!value) return "";
  if (/^(https?:)?\/\//.test(value) || value.startsWith("/") || value.startsWith("data:") || value.startsWith("mailto:")) {
    return value;
  }
  return `../../${value.replace(/^\.?\//, "")}`;
}

