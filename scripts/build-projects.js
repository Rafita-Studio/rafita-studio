function writeProjectPages(projects) {
  fs.mkdirSync(projectPagesDir, { recursive: true });
  projects.forEach((project) => {
    fs.writeFileSync(path.join(projectPagesDir, `${project.slug}.html`), renderProjectPage(project));
  });
}

function cleanGeneratedProjectPages() {
  if (!fs.existsSync(projectPagesDir)) return;

  fs.readdirSync(projectPagesDir)
    .filter((file) => path.extname(file).toLowerCase() === ".html")
    .forEach((file) => {
      const filePath = path.join(projectPagesDir, file);
      const contents = fs.readFileSync(filePath, "utf8");
      if (contents.includes(generatedMarker)) {
        fs.unlinkSync(filePath);
      }
    });
}

function main() {
  fs.mkdirSync(dataDir, { recursive: true });
  fs.mkdirSync(projectPagesDir, { recursive: true });

  const files = fs.existsSync(projectsDir)
    ? fs.readdirSync(projectsDir)
        .filter((file) => path.extname(file).toLowerCase() === ".md")
        .map((file) => path.join(projectsDir, file))
    : [];

  const projects = files
    .map(normalizeProject)
    .filter((project) => project.published)
    .sort(sortProjects);

  const jsonProjects = projects.map(({ body, bodyHtml, ...project }) => project);

  fs.writeFileSync(outputJsonPath, `${JSON.stringify(jsonProjects, null, 2)}\n`);
  cleanGeneratedProjectPages();
  writeProjectPages(projects);

  console.log(`Built ${projects.length} published project(s).`);
  console.log(`Wrote ${path.relative(rootDir, outputJsonPath)}`);
  console.log(`Wrote project pages in ${path.relative(rootDir, projectPagesDir)}`);
}

main();
